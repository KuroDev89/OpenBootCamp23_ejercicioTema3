public class FuncSuma {
    //Primera Parte
    public static int sumar(int a, int b, int c) {
        return a + b + c;
    }

    //MAIN
    public static void main(String[] args) {
        int resultado = sumar(5, 12, 8);
        System.out.println("El resultado de la suma es: " + resultado);
    }

}